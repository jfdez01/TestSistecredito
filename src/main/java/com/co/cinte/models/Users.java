package com.co.cinte.models;

public class Users {

	private String userAdmin;
	private String passwordAdmin;
	
	private String userAnalyst;
	private String passwordAnalyst;
	public String getUserAdmin() {
		return userAdmin;
	}
	public void setUserAdmin(String userAdmin) {
		this.userAdmin = userAdmin;
	}
	public String getPasswordAdmin() {
		return passwordAdmin;
	}
	public void setPasswordAdmin(String passwordAdmin) {
		this.passwordAdmin = passwordAdmin;
	}
	public String getUserAnalyst() {
		return userAnalyst;
	}
	public void setUserAnalyst(String userAnalyst) {
		this.userAnalyst = userAnalyst;
	}
	public String getPasswordAnalyst() {
		return passwordAnalyst;
	}
	public void setPasswordAnalyst(String passwordAnalyst) {
		this.passwordAnalyst = passwordAnalyst;
	}
}
